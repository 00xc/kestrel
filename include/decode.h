static inline int uri_decode(char *str)
{
	char *src = str;
	char *dst = str;
	char dec;
	int lo, hi;

	while (*src) {
		if (*src != '%') {
			*dst++ = *src++;
			continue;
		}

		hi = hex_table[(unsigned char)src[1]];
		if (hi < 0)
			return -1;
		lo = hex_table[(unsigned char)src[2]];
		if (lo < 0)
			return -1;

		dec = (char)((hi << 4) | lo);
		if (dec == '\0')
			return -1;

		*dst++ = dec;
		src += 3;
	}
	*dst = '\0';
	return 0;
}

